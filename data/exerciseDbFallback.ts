export const EXERCISE_BY_TARGET: { [key: string]: ExerciseDBExercise[] } = {};
export const EXERCISE_BY_EQUIPMENT: { [key: string]: ExerciseDBExercise[] } = {};

// Build lookup maps
EXERCISE_DB_FALLBACK.forEach(exercise => {
  // By body part
  if (!EXERCISE_BY_BODY_PART[exercise.bodyPart]) {
    EXERCISE_BY_BODY_PART[exercise.bodyPart] = [];
  }
  EXERCISE_BY_BODY_PART[exercise.bodyPart].push(exercise);

  // By target
  if (!EXERCISE_BY_TARGET[exercise.target]) {
    EXERCISE_BY_TARGET[exercise.target] = [];
  }
  EXERCISE_BY_TARGET[exercise.target].push(exercise);

  // By equipment
  if (!EXERCISE_BY_EQUIPMENT[exercise.equipment]) {
    EXERCISE_BY_EQUIPMENT[exercise.equipment] = [];
  }
  EXERCISE_BY_EQUIPMENT[exercise.equipment].push(exercise);
});
